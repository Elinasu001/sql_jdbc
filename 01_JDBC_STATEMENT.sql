DROP TABLE BOARD;
CREATE TABLE BOARD(
    BOARD_NO NUMBER PRIMARY KEY,
    BOARD_TITLE NVARCHAR2(300) NOT NULL, -- 사용자에게 받아서 작업
    BOARD_CONTENT NVARCHAR2(1000) NOT NULL, -- 사용자에게 받아서 작업
    BOARD_WRITER NUMBER REFERENCES MEMBER(USERNO), -- 외래키 -- 사용자에게 받아서 작업
    CREATE_DATE DATE DEFAULT SYSDATE,
    DELETE_YN CHAR(1) DEFAULT 'N'	-- 삭제 여부 컬럼
);

