DROP TABLE MEMBER;
CREATE TABLE MEMBER(
    USERNO NUMBER PRIMARY KEY,
    USERID VARCHAR2(15) UNIQUE NOT NULL, -- 중복방지
    USERPWD VARCHAR2(20) NOT NULL,
    USERNAME VARCHAR2(15) NOT NULL,
    EMAIL VARCHAR2(30),
    ENROLLDATE DATE DEFAULT SYSDATE NOT NULL -- 시퀀스로 만들 예정
);

