  START WITH 1
  NOCACHE;
CREATE TABLE TB_STUDENTS (
    STUDENT_ID NUMBER PRIMARY KEY,
    STUDENT_NAME NVARCHAR2(20) NOT NULL,
    BIRTH_DATE DATE NOT NULL,
    GENDER CHAR(1) CHECK (GENDER IN ('M', 'F')) NOT NULL,
    ENROLL_DATE DATE NOT NULL,
    DELETE_YN CHAR(1) DEFAULT 'N',	-- 삭제 여부 컬럼
    MAJOR_ID NUMBER,
    DORM_ID NUMBER,
    CONSTRAINT FK_TB_STUDENTS_MAJAR FOREIGN KEY (MAJOR_ID) REFERENCES TB_MAJOR(MAJOR_ID),
    CONSTRAINT FK_TB_STUDENTS_DORM FOREIGN KEY (DORM_ID) REFERENCES TB_DORMITORY(DORM_ID)
);

