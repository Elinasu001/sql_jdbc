SELECT
       BOARD_NO
     , BOARD_TITLE
     , COUNT
 FROM
      KH_BOARD
 JOIN
      KH_ATTACHMENT ON (REF_BNO = BOARD_NO)
WHERE
      BOARD_TYPE = 2
  AND
      KH_BOARD.STATUS = 'Y'
ORDER
   BY 
      BOARD_NO DESC;



